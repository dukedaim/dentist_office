function toggleForm() {
    let form = document.getElementById("updateForm");
    form.style.display = (form.style.display === "none") ? "block" : "none";
}

function toggleAppointForm() {
    let form = document.getElementById("showAppointForm");
    form.style.display = (form.style.display === "none") ? "block" : "none";
}